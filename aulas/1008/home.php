<p>
A série se passa no ano de 1983 e é altamente tematizada pelos elementos culturais da década, com uma trilha sonora toda remetente aos marcantes sintetizadores da época e inúmeras referências a obras de Steven Spielberg,[3] John Carpenter[4] e Stephen King,[5] considerados as grandes inspirações dos Irmãos Duffer para a realização do projeto.[6] Na trama, um garoto desaparece misteriosamente na pequena cidade de Hawkins, Indiana, e faz seus amigos partirem por sua busca, que no caminho encontram uma estranha menina com poderes telecinéticos.
